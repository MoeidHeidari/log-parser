/**
 * An enum that represents the  log levels the parser understands
 */
export enum LogLevel {
  /**
   * Error log level
   */
  ERROR = 'error',
  /**
   * Info log level
   */
  INFO = 'info',
  /**
   * Warning log level
   */
  WARN = 'warn',
  /**
   * Debug log level
   */
  DEBUG = 'debug',
}
